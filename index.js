var L = require('leaflet');


// *******************************************************************************************************************************
// *****************************************************       CREACION DEL MAPA       *******************************************
// *******************************************************************************************************************************

var map = L.map('map', {
	scrollWheelZoom: true,
	center: [-9, -74],
	zoom: 6,
	zoomControl: false,
	fullscreenControl: true,
	fullscreenControlOptions: {
		position: 'bottomleft'
	}
});

// ******************************************************************************************************************************
// ******************************************************************************************************************************

// Configurar poicion del zoom control
L.control.zoom({
     position:'bottomleft'
}).addTo(map);



// Asignar mapas base
var osm = L.tileLayer('http://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
	attribution: '&copy; <a href="http://openstreetmap.org">OpenStreetMap</a> Contributors'
}).addTo(map);

// ******************************************************************************************************************************
// *****************************************************          WIDGET HOME         *******************************************
// ******************************************************************************************************************************

function zoomhome(){
	map.setView(new L.LatLng(-9, -74), 6);
}

document.getElementById("widget-home").addEventListener("click", zoomhome);

